    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Declaracion de variables
        double disc, discS, x1, x2;
        // Dar formato de dos digitos al resultado .##
        DecimalFormat frm = new DecimalFormat(".##");
        // Recibir el valor del TextField y transformarlo a entero
        float a = Float.parseFloat(valora.getText());
        float b = Float.parseFloat(valorb.getText());
        float c = Float.parseFloat(valorc.getText());
        
        // Calculo del discriminate sin raiz de la formula general
        discS = ((b*b)-(4*a*c));
        // Calculo del discriminante (junto a la raiz) de la formula general
        disc = Math.sqrt((b*b)-(4*a*c));
        
        // Calculo de los dos resultados reales
        x1 = (((-b)+disc)/(2*a));
        x2 = (((-b)-disc)/(2*a));
            
        // Condicion para que efectue el proceso si el discriminante es igual a cero
        if (disc == 0){
            
            JOptionPane.showMessageDialog(null,"Moris Josu√© Hern√°ndez de la Cruz\n"
                    + "Discriminante igual a 0, existe un √∫nico valor para x1 y x2");
            valx1.setText(""+frm.format(x1));
            valx2.setText(""+frm.format(x2));
            valord.setText(""+frm.format(discS));
        }
        // Condicion para que efectue el proceso si el discriminante es mayor a cero
        else if (disc > 0){
            
            valx1.setText(""+frm.format(x1));
            valx2.setText(""+frm.format(x2));
            valord.setText(""+frm.format(discS));
            JOptionPane.showMessageDialog(null,"Moris Josu√© Hern√°ndez de la Cruz\n"
                    + "Discriminante mayor que 0, existen dos resultados reales diferentes");
            
        }
        else{
            // Sino, se le envia un mensaje al usuario informando que no existe solucion real
            JOptionPane.showMessageDialog(null,"Moris Josu√© Hern√°ndez de la Cruz\n"
                    + "Discriminante menor que 0, no existe soluci√≥n real");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

